                    alert('You pressed on Row: ' + data[0] + ' ' + data[1] + ' ' + data[2] + '\'s row.');
                });

                // Delete a record
                table.on('click', '.remove', function(e) {
                    $tr = $(this).closest('tr');
                    if ($($tr).hasClass('child')) {
                        $tr = $tr.prev('.parent');
                    }
                    table.row($tr).remove().draw();
                    e.preventDefault();
                });

                // Like record
                table.on('click', '.like', function() {
                    alert('You clicked on Like button');
                });
            }

